package util;

public class Grade {
	private int Sid;
	private String Sname;
	private String Ssex;
	private String Sclas;
	private int Smath;
	private int Schinese;
	public int getSid() {
		return Sid;
	}
	public void setSid(int sid) {
		Sid = sid;
	}
	public String getSname() {
		return Sname;
	}
	public void setSname(String sname) {
		Sname = sname;
	}
	public String getSsex() {
		return Ssex;
	}
	public void setSsex(String ssex) {
		Ssex = ssex;
	}
	public String getSclas() {
		return Sclas;
	}
	public void setSclas(String sclas) {
		Sclas = sclas;
	}
	public int getSmath() {
		return Smath;
	}
	public void setSmath(int smath) {
		Smath = smath;
	}
	public int getSchinese() {
		return Schinese;
	}
	public void setSchinese(int schinese) {
		Schinese = schinese;
	}
	
}
